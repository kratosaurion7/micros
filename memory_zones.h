#ifndef MEMORYZONES_H
#define MEMORYZONES_H

// BIOS
#define BIOS_LOCATION (1024*1024*0)
#define BIOS_END (BIOS_LOCATION + (1024*1024*1))
#define BIOS_LENGTH (BIOS_END - BIOS_LOCATION)

// Sections
#define SECT_LOCATION (1024*1024*1)
#define SECT_END (SECT_LOCATION + (1024*1024*1))
#define SECT_LENGTH (SECT_END - SECT_LOCATION)

// Paging
#define PFM_LOCATION (1024*1024*2) // Page #512
#define PFM_END (PFM_LOCATION + (1024*1024*1))
#define PFM_LENGTH (PFM_END - PFM_LOCATION)

#define KPT_LOCATION (1024*1024*4) //Page #1024
#define KPT_END (KPT_LOCATION + (1024*1024*4))
#define KPT_LENGTH (KPT_END - KPT_LOCATION)

// Modules
#define MODULES_LOCATION (1024*1024*9)
#define MODULES_END (MODULES_LOCATION + (1024*1024*1))
#define MODULES_LENGTH (MODULES_END - MODULES_LOCATION)

// Memory
#define KERNEL_HEAP_START (1024*1024*10) // Page #1536
#define KERNEL_HEAP_END (KERNEL_HEAP_START + (1024*1024*50))
#define KERNEL_HEAP_LENGTH (KERNEL_HEAP_END - KERNEL_HEAP_START)


#endif
